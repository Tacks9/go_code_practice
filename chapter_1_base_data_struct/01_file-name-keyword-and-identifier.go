/*
Go 源文件：
	文件名(小写字母,可用下划线连接) + 后缀名(.go结尾)

标识符：
	一般标识符：
		1、区分大小写
		2、字母开头，后面可以跟数字下划线
		3、关键字不能作为标识符，比如一些保留字，预定义标识符，内置函数名等
	特别标识符：
		1、_，也就是一个下划线，也是叫做空白标识符
		2、任何类型的值都可以赋值给他，但是最终都将会被抛弃，也就是之后不能使用这个标识符进行运算
		3、其实有点类似垃圾桶，计算出来某个结果不想要的话，就丢进去

关键字：
	1、逻辑运算符号
	2、数据类型符号
	3、内置函数符号
	4、等等

程序：
	1、程序由算法+数据结构组成
	2、代码一般包含，关键字、常量、变量、运算符、函数、注释等
	3、每一行代码也就是一个语句，结尾不需要使用;分号进行结束，这些都将由Go编译器自动完成




*/
